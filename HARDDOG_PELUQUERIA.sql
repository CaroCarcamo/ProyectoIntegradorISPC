#CREO LA BASE DE DATOS
DROP DATABASE HARDDOG_PELUQUERIA;
CREATE DATABASE HARDDOG_PELUQUERIA;
USE HARDDOG_PELUQUERIA;
#CREO TABLA DUENO
CREATE TABLE DUENO
(DNI int,
NOMBRE VARCHAR (50),
APELLIDO VARCHAR (30),
TELEFONO VARCHAR (20),
DIRECCION VARCHAR (50),
constraint pk_dni primary key (DNI));
#CREO TABLA PERRO
CREATE TABLE PERRO
(ID_PERRO VARCHAR (10),
NOMBRE VARCHAR (20),
FECHA_NAC DATE,
SEXO VARCHAR (1),
DNI_DUENO INT,
constraint PK_IDP PRIMARY KEY (ID_PERRO),
CONSTRAINT FK_FDNI FOREIGN KEY (DNI_DUENO) REFERENCES DUENO (DNI));
#CREO TABLA HISTORIAL
CREATE TABLE HISTORIAL
(ID_HISTORIAL varchar (10),
FECHA date,
PERRO VARCHAR (20),
DESCRIPCION VARCHAR (300),
MONTO FLOAT,
CONSTRAINT PK_IDH PRIMARY KEY (ID_HISTORIAL),
constraint FK_FPERRO FOREIGN KEY (PERRO) REFERENCES PERRO (ID_PERRO));
